char	*ft_strcapitalize(char *s)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		if (i == 0 && s[i] >= 'a' && s[i] <= 'z')
			s[i] = s[i] - 32;
		if ((s[i] < '0' || (s[i] > '9' && s[i] < 'A') ||
			(s[i] > 'Z' && s[i] < 'a') ||
			s[i] > 'z') && s[i + 1] >= 'a' && s[i + 1] <= 'z')
			s[i + 1] = s[i + 1] - 32;
		i++;
	}
	return (s);
}
